package net.minecraft.src;

public class MEMCollisionObject {
	ChunkCoordinates pos;
	EntityMEMBlockWrapper entity;

	@Override
	public int hashCode() {
		final int prime = 31;
		int result = 1;
		result = prime * result + ((entity == null) ? 0 : entity.hashCode());
		result = prime * result + ((pos == null) ? 0 : pos.hashCode());
		return result;
	}

	@Override
	public boolean equals(Object obj) {
		if (this == obj)
			return true;
		if (obj == null)
			return false;
		if (getClass() != obj.getClass())
			return false;
		MEMCollisionObject other = (MEMCollisionObject) obj;
		if (entity == null) {
			if (other.entity != null)
				return false;
		} else if (!entity.equals(other.entity))
			return false;
		if (pos == null) {
			if (other.pos != null)
				return false;
		} else if (!pos.equals(other.pos))
			return false;
		return true;
	}

	public MEMCollisionObject(EntityMEMBlockWrapper entity, ChunkCoordinates pos) {
		super();
		this.pos = pos;
		this.entity = entity;
	}
}
